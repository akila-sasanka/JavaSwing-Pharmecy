    private int IsAdded(Object [] r) {
        DefaultTableModel model=(DefaultTableModel) tblbill.getModel();
         if(tblbill.getRowCount()>0){
           for (int i = 0; i <tblbill.getRowCount(); i++) {
           if(tblbill.getValueAt(i, 0).equals(r[0].toString())){
           return i;
          } 
        }
     }
    return -1;
   }
