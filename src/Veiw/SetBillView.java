    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        DefaultTableModel model=(DefaultTableModel) tblbill.getModel();
        double m=0;
        
        try {
            m= new MedicinController().getPrice(txtSearch.getText());
            if(m==0){
                JOptionPane.showMessageDialog(null," Medecine Doesn't exisit try again");
                return;
            }
            m*=Double.parseDouble(txtQuantity.getText());
            sum+=m;
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SetBillView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(SetBillView.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        Object [] r={txtSearch.getText(),txtQuantity.getText(),m};
        int x=IsAdded(r);
        if(x>=0){
           int c=Integer.parseInt(txtQuantity.getText());
           c+=Integer.parseInt(model.getValueAt(x, 1).toString());
           model.setValueAt(c,x, 1);
       }else{
       model.addRow(r);
       }
       
        txtAmount.setText(String.valueOf(sum));
        clearTableTxt();
        // TODO add your handling code here:
    }//GEN-LAST:event_btnAddActionPerformed

