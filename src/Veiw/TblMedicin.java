    public void updateTable() throws ClassNotFoundException, SQLException {
        ResultSet set=new MedicinController().getMedicins();
        DefaultTableModel model=(DefaultTableModel)tblMedicen.getModel();
        tblMedicen.removeAll();
        while(set.next()){
            Object [] row={set.getString(1),set.getString(2),set.getString(3),
                set.getString(4),set.getString(5),set.getString(6)};
            model.addRow(row);
        }
    }
