        int row=tblMedicen.getSelectedRow();
        DefaultTableModel m=(DefaultTableModel) tblMedicen.getModel();   
        String mName=tblMedicen.getValueAt(row, 0).toString();
        String mCategory=tblMedicen.getValueAt(row, 1).toString();
        String mId=tblMedicen.getValueAt(row, 2).toString();
